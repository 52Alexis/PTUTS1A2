                    <p>Voici les résultats de notre sondage (voir annexe I) sur la question du prix des logiciels :</p>
                    <p>Comme on peut le constater, sur 12 réponses, 8 personnes pensent que le prix des logiciels de MAO est trop élevé, 2 personnes pensent qu’il est moyen et 2 personnes pensent qu’il est raisonnable. On voit bien qu’une majorité de personnes pensent ces logiciels trop chers.</p>
                    <p>Il existe quelques logiciels gratuits mais ils ne sont pas aussi pratiques que ceux qui sont payants. Voici une citation qui image bien la situation : « Paris-Lyon en vélo: c’est gratuit. N’empêche que la plupart des gens préfèrent payer pour faire le trajet en avion, train ou voiture: plus pratique, plus rapide. », de Julien Moulinié-Chaumel, provenant de l’article « Logiciel MAO: Le guide des 11 meilleurs DAW en 2020 ».</p>
                    <p>Les séquenceurs proposent généralement plusieurs éditions différentes avec des prix grandissants et des fonctionnalités en plus.</p>
                    <p>Dans le cas de ces séquenceurs, des packs de sons, d’effets ou d’instruments sont disponibles à l’achat, même si de nombreux packs sont gratuits. Ces packs peuvent être créés soit par des entreprises professionnelles soit par des particuliers qui « samplent », c’est-à-dire ils enregistrent et échantillonnent des sons à l’aide d’un sampler, des sonorités pour pouvoir ensuite les réutiliser.</p>
                    <p>Les droits appliqués aux licences sont en général ceux du pays de l’entreprise. Lors de l’achat du logiciel, l’utilisateur obtient les droits de licence octroyés par l’entreprise sur son logiciel (voir annexe II). Ces licences sont soumises à des restrictions qui garantissent son intégrité et son développement privé. Par exemple, en tant que titulaire d’une licence nous ne sommes pas autorisés à décompiler et à désassembler le logiciel.</p>
                    <p>C’est pourquoi il existe des versions piratées, gratuites mais illégales de ces logiciels. Cependant, le piratage est prohibé, en France, les développeurs de logiciels avec licence sont protégés par des lois qui régissent les droits d’auteur, la propriété intellectuelle et la protection juridique des programmes d’ordinateur. Les articles L.335-3, L.122-4 et L.335-2 du code de la propriété intellectuelle interdisent la copie et l’installation d’un logiciel sans l’accord du détenteur des droits d’auteur, et définissent les sanctions encourues.</p>
                    <p>La loi punit sévèrement le piratage de logiciel, une personne physique risque une amende qui peut aller jusqu'à 150 000 euros et une peine d’emprisonnement de 2 ans maximum. Si le piratage a été fait dans un cadre professionnel, l’employé encourt le risque supplémentaire d’être licencié. Pour une personne morale, les sanctions possibles comprennent une amende pouvant s’élever jusqu’à 750 000 euros, la confiscation du matériel ayant servi au piratage, l’interdiction d’exercer temporairement voire définitivement une ou plusieurs activités professionnelles, l’interdiction d’émettre des chèques pendant au moins 5 ans et même le placement sous surveillance judiciaire.Revenons-en aux résultats de notre sondage :</p>
